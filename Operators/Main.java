public class Main
{
	public static void main(String[] args)
	{
		// Arithmatic Operator
		double a = 1;
		double b = 2;
		double div = a / b;
		System.out.println(div);
		
		// Unary Operator
		// Pre increment
		int num1 = 1;
		System.out.println(++num1); // 2
		System.out.println(num1); // 2
		
		// Post increment
		int num2 = 1;
		System.out.println(num2++); // 1
		System.out.println(num2); // 2
		
	}
}